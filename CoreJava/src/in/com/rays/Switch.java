package in.com.rays;

public class  Switch {
public static void main(String[] args) {
	int dayofweak=6;
	switch(dayofweak) {
	case 1:
		System.out.println("monday");
		break;
	case 2:  
		System.out.println("tuesday");
		break;
	case 3:
		System.out.println("wednsday");
		break;
	default:
		System.out.println("default part");
		break;
	}
}
}

